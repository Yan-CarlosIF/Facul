# Definindo algumas variáveis
x = 5
y = 3

# Adição
resultado_adicao = x + y
print(f"Resultado de {x} + {y}: {resultado_adicao}")  # 8

# Subtração
resultado_subtracao = x - y
print(f"Resultado de {x} - {y}: {resultado_subtracao}")  # 2

# Multiplicação
resultado_multiplicacao = x * y
print(f"Resultado de {x} * {y}: {resultado_multiplicacao}")  # 15

# Divisão
resultado_divisao = x / y
print(f"Resultado de {x} / {y}: {resultado_divisao}")  # 1.6666666666666667

# Divisão inteira
resultado_divisao_inteira = x // y
print(f"Resultado de {x} // {y}: {resultado_divisao_inteira}")  # 1

# Módulo (Resto da divisão)
resultado_modulo = x % y
print(f"Resultado de {x} % {y}: {resultado_modulo}")  # 2

# Exponenciação
resultado_exponenciacao = x ** y
print(f"Resultado de {x} ** {y}: {resultado_exponenciacao}")  # 125